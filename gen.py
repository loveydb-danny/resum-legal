def esc(s):
    return html.escape(s, quote=False)

def render_blocks(blocks):
    out = []
    for b in blocks:
        kind = b[0]
        if kind == "note":
            out.append(f'<p class="intro">{esc(b[1])}</p>')
        elif kind == "p":
            out.append(f"<p>{esc(b[1])}</p>")
        elif kind == "pb":  # paragraph with bold prefix
            out.append(f"<p><strong>{esc(b[1])}</strong> {esc(b[2])}</p>")
        elif kind == "h2":
            out.append(f"<h2>{esc(b[1])}</h2>")
        elif kind == "h3":
            out.append(f"<h3>{esc(b[1])}</h3>")
        elif kind in ("ol", "ul"):
            tag = "ol" if kind == "ol" else "ul"
            cls = "" if kind == "ol" else ' class="plain"'
            lis = []
            for it in b[1]:
                if isinstance(it, tuple):
                    lis.append(f"<li><strong>{esc(it[0])}</strong> {esc(it[1])}</li>")
                else:
                    lis.append(f"<li>{esc(it)}</li>")
            out.append(f"<{tag}{cls}>" + "".join(lis) + f"</{tag}>")
        elif kind == "sub":  # nested bullet list under previous list item
            lis = "".join(f"<li>{esc(x)}</li>" for x in b[1])
            out.append(f'<ul class="plain sub">{lis}</ul>')
        elif kind == "subkv":  # nested bullet list with bold prefix
            lis = "".join(f"<li><strong>{esc(k)}</strong> {esc(v)}</li>" for k, v in b[1])
            out.append(f'<ul class="plain sub">{lis}</ul>')
        elif kind == "table":
            header, rows = b[1], b[2]
            thead = "<tr>" + "".join(f"<th>{esc(h)}</th>" for h in header) + "</tr>"
            trs = []
            for r in rows:
                trs.append("<tr>" + "".join(f"<td>{esc(c)}</td>" for c in r) + "</tr>")
            out.append('<div class="tablewrap"><table><thead>' + thead +
                       "</thead><tbody>" + "".join(trs) + "</tbody></table></div>")
        elif kind == "company":
            rows = "".join(f"<div><dt>{esc(k)}</dt><dd>{esc(v)}</dd></div>" for k, v in b[1])
            out.append(f'<dl class="company">{rows}</dl>')
    return "\n".join(out)

